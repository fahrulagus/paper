<p>Teori Dempster-Shafer adalah suatu teori matematika untuk pembuktian (Kusumadewi, 2003) berdasarkan belief functions and plausible reasoning (fungsi kepercayaan dan pemikiran yang masuk akal), yang digunakan untuk mengkombinasikan potongan informasi yang terpisah (bukti) untuk mengkalkulasi kemungkinan dari suatu peristiwa. Teori ini dikembangkan oleh Arthur P. Dempster dan Glenn Shafer.</p>

<p>Merupakan model ketidakpastian yang menggunakan rentang probabilitas. Eviden dibagi-bagi secara terpisah, kemudian dihitung nilai probabilitasnya. Hal ini mengakibatkan perhitungan bilangan yang agak rumit, dan dalam kasus permasalahan yang besar, perhitungan nilai ketidakpastiannya akan semakin komplek. Dilihat dari sistem rentang yang digunakan, maka metode faktor kepastian (CF) dapat dipandang sebagai kasus khusus dari teori Dempster-Shafer.</p>

<p>Ada berbagai macam penalaran dengan model yang lengkap dan sangat konsisten, tetapi pada kenyataannya banyak permasalahan yang tidak dapat terselesaikan secara lengkap dan konsisten. Ketidakkonsistenan yang tersebut adalah akibat adanya penambahan fakta baru. Penalaran yang seperti itu disebut dengan penalaran non monotonis. Untuk mengatasi ketidakkonsistenan tersebut maka dapat menggunakan penalaran dengan teori Dempster-Shafer. Secara umum teori Dempster-Shafer ditulis dalam suatu interval:
<br /><br />
<strong>[Belief,Plausibility]</strong>

    <p>Belief (Bel) adalah ukuran kekuatan evidence dalam mendukung suatu himpunan proposisi. Jika bernilai 0 maka mengindikasikan bahwa tidak ada evidence, dan jika bernilai 1 menunjukkan adanya kepastian. Dimana nilai bel yaitu (0-0.9).</p>
    
    <p>Plausibility (Pl) dinotasikan sebagai : Pl(s) = 1 - Bel (-s) Plausibility juga bernilai 0 sampai 1. Jika yakin akan -s, maka dapat dikatakan bahwa Bel(-s)=1, dan Pl(-s)=0.</p>

